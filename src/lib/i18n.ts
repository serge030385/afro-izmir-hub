export const supportedLanguages = ["fr", "en", "tr"] as const;
export const defaultLanguage = "fr";

export type Language = (typeof supportedLanguages)[number];
export type LocalizedText = Record<Language, string>;

export function isSupportedLanguage(value: string): value is Language {
  return supportedLanguages.includes(value as Language);
}

export function localized(fr: string, en: string, tr: string): LocalizedText {
  return { fr, en, tr };
}

export function getLocalized(text: LocalizedText, language: Language) {
  return text[language] ?? text[defaultLanguage];
}

